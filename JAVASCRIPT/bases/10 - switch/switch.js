

const dia = 2

switch ( dia ) { // equivalente a un triple ===
    case 0:
        console.log('Domingo');
        break
    case 1:
        console.log('Lunes');
        break
    case 2:
        console.log('Martes');
        break
    
    default:
        console.log('No es lunes/martes/domingo');
        break
}


